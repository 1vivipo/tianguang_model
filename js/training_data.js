// 获取所有数据
function getAllData() {
    const all = [];
    for (const category in BUILTIN_DATA) {
        all.push(...BUILTIN_DATA[category]);
    }
    return all;
}

// 获取随机数据
function getRandomData(count = 50) {
    const all = getAllData();
    const shuffled = [...all].sort(() => Math.random() - 0.5);
    return shuffled.slice(0, count);
}

// 获取分类数据
function getCategoryData(category) {
    return BUILTIN_DATA[category] || [];
}

