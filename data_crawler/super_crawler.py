@dataclass
class SuperCrawler:
    """超级爬虫"""
    id: int
    keyword: str
    
    def crawl(self) -> List[Dict]:
        """爬取"""
        results = []
        
        try:
            # 使用DuckDuckGo API
            url = f"https://api.duckduckgo.com/?q={urllib.parse.quote(self.keyword)}&format=json&no_html=1"
            
            req = urllib.request.Request(
                url,
                headers={'User-Agent': 'Mozilla/5.0 (Windows NT 10.0; Win64; x64) AppleWebKit/537.36'}
            )
            
            with urllib.request.urlopen(req, timeout=15) as response:
                data = json.loads(response.read().decode())
            
            if data.get('AbstractText'):
                results.append({
                    "id": f"crawl_{self.id}",
                    "keyword": self.keyword,
                    "content": data['AbstractText'],
                    "source": data.get('AbstractURL', ''),
                    "crawled_at": datetime.now().isoformat()
                })
            
            # 相关主题
            for topic in data.get('RelatedTopics', [])[:2]:
                if isinstance(topic, dict) and 'Text' in topic:
                    results.append({
                        "id": f"crawl_{self.id}_{len(results)}",
                        "keyword": self.keyword,
                        "content": topic['Text'],
                        "source": topic.get('FirstURL', ''),
                        "crawled_at": datetime.now().isoformat()
                    })
        
        except Exception as e:
            pass
        
        return results


class SuperCrawlerSystem:
    """超级爬虫系统"""
    
    def __init__(self):
        # 生成关键词
        self.keywords = generate_10000_keywords()
        
        # 创建爬虫
        self.crawlers = [
            SuperCrawler(id=i, keyword=kw)
            for i, kw in enumerate(self.keywords)
        ]
        
        # 数据
        self.all_data = []
        self.round_count = 0
        
        # 线程池
        self.executor = ThreadPoolExecutor(max_workers=MAX_WORKERS)
        
        print(f"✓ 创建了 {len(self.crawlers)} 只爬虫！")
    
    def crawl_round(self) -> int:
        """抓一把"""
        self.round_count += 1
        start_time = time.time()
        
        print(f"\n{'='*60}")
        print(f"  第 {self.round_count} 把 - {len(self.crawlers)} 只虫子出击！")
        print(f"{'='*60}")
        
        # 随机选择一批爬虫
        batch_size = min(1000, len(self.crawlers))  # 每把1000只
        batch = random.sample(self.crawlers, batch_size)
        
        # 并行爬取
        futures = []
        for crawler in batch:
            future = self.executor.submit(crawler.crawl)
            futures.append(future)
        
        # 收集结果
        count = 0
        for future in as_completed(futures):
            try:
                results = future.result()
                self.all_data.extend(results)
                count += len(results)
            except:
                pass
        
        elapsed = time.time() - start_time
        
        print(f"  ✓ 本次抓取: {count} 条")
        print(f"  ✓ 累计数据: {len(self.all_data)} 条")
        print(f"  ✓ 用时: {elapsed:.1f} 秒")
        
        return count
    
    def save(self):
        """保存数据"""
        if not self.all_data:
            return
        
        # 保存
        filename = f"super_crawl_round{self.round_count}_{datetime.now().strftime('%Y%m%d_%H%M%S')}.json"
        filepath = os.path.join(DATA_DIR, filename)
        
        with open(filepath, 'w', encoding='utf-8') as f:
            json.dump(self.all_data, f, ensure_ascii=False, indent=2)
        
        print(f"  ✓ 保存到: {filepath}")
        
        # 同时保存到黑洞知识库
        blackhole_dir = os.path.join(REPO_DIR, "blackhole_llm", "blackhole_knowledge")
        os.makedirs(blackhole_dir, exist_ok=True)
        
        blackhole_file = os.path.join(blackhole_dir, filename)
        with open(blackhole_file, 'w', encoding='utf-8') as f:
            json.dump(self.all_data, f, ensure_ascii=False, indent=2)
        
        print(f"  ✓ 同步到黑洞知识库")
    
    def run(self, rounds: int = 10):
        """运行"""
        print(f"""
╔══════════════════════════════════════════════════════════╗
║            超级爬虫系统 - 10000只虫子                   ║
╠══════════════════════════════════════════════════════════╣
║  爬虫数量: {len(self.crawlers):<42} ║
║  每把数量: 1000只{' '*35} ║
║  抓取轮数: {rounds}把{' '*40} ║
║  预计数据: {rounds * 2000}+条{' '*36} ║
╚══════════════════════════════════════════════════════════╝
""")
        
        for i in range(rounds):
            self.crawl_round()
            self.save()
            
            if i < rounds - 1:
                print(f"\n等待 {CRAWL_INTERVAL} 秒后抓下一把...")
                time.sleep(CRAWL_INTERVAL)
        
        print(f"\n{'='*60}")
        print(f"  全部完成！")
        print(f"  总数据: {len(self.all_data)} 条")
        print(f"{'='*60}")


