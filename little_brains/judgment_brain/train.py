import os
import json
from typing import List, Dict
from enum import Enum


class JudgmentType(Enum):
    TRUE_FALSE = "真假判断"
    GOOD_BAD = "好坏判断"
    RIGHT_WRONG = "对错判断"
    POSSIBLE = "可能性判断"
    SUITABLE = "适合性判断"


class JudgmentBrain:
    """判断脑"""
    
    def __init__(self):
        self.knowledge: Dict[str, str] = {}
        self.stats = {"total": 0, "correct": 0}
    
    def judge(self, question: str) -> Dict:
        """做出判断"""
        self.stats["total"] += 1
        
        # 查知识库
        for k, v in self.knowledge.items():
            if k in question:
                self.stats["correct"] += 1
                return {"question": question, "answer": v, "found": True}
        
        # 默认判断
        return {"question": question, "answer": "需要更多信息才能判断", "found": False}
    
    def learn(self, question: str, answer: str):
        """学习"""
        self.knowledge[question] = answer
    
    def save(self, path: str):
        with open(path, 'w', encoding='utf-8') as f:
            json.dump({"knowledge": self.knowledge, "stats": self.stats}, f, ensure_ascii=False, indent=2)


